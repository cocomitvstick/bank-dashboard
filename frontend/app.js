/** Возвращает все "листовые" компоненты метрики (без isGroup-заголовков) */
function getF101Leaves(metric) {
  const comps = F101_COMPONENTS[metric];
  if (!comps) return [];
  const leaves = [];
  for (const c of comps) {
    if (c.isGroup && c.children) {
      // Если у группы defaultOn:true — сам родительский агрегат является активным листом
      // (ЦБ возвращает агрегат, не детализацию; 45.2 надёжнее суммы 455+454)
      if (c.defaultOn !== false) {
        leaves.push({ code: c.code, ap: c.ap, sign: c.sign || 1,
                      label: c.label, color: c.color, defaultOn: true, isParentAgg: true });
      }
      // Дочерние коды всегда включаем как потенциальные переключатели
      for (const child of c.children) {
        leaves.push({ ...child, groupLabel: c.label, parentKey: `${c.code}:${c.ap}` });
      }
    } else if (!c.isGroup) {
      leaves.push(c);
    }
  }
  return leaves;
}

