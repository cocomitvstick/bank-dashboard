function fmt(val, metric) {
  if (val == null) return '—';
  const unit = METRIC_UNITS[metric] || '';
  if (unit === 'млрд ₽') {
    if (val >= 1000) return (val / 1000).toFixed(1) + ' трлн';
    return val.toFixed(1);
  }
  return val.toFixed(2) + '%';
}

function fmtNum(val) {
  if (val == null) return '—';
  if (Math.abs(val) >= 1000) return (val / 1000).toFixed(1) + 'T';
  return val.toFixed(1);
}

function fmtDelta(delta) {
  if (!delta || delta.pct == null) return { text: '—', cls: 'neu' };
  const sign = delta.pct > 0 ? '+' : '';
  const cls  = delta.pct > 0 ? 'pos' : (delta.pct < 0 ? 'neg' : 'neu');
  return { text: `${sign}${delta.pct.toFixed(1)}%`, cls };
}

/** Форматирует дату предыдущего периода: "2026-01-01" → "01.2026" */
const _MONTH_NAMES_SHORT = ['янв','фев','мар','апр','май','июн','июл','авг','сен','окт','ноя','дек'];
function fmtPrevDate(dateStr) {
  if (!dateStr) return 'пред. мес.';
  const parts = dateStr.split('-');
  if (parts.length < 2) return dateStr;
  const y = parts[0];
  const m = parseInt(parts[1], 10);
  return `${_MONTH_NAMES_SHORT[m - 1]}.${y}`;
}

