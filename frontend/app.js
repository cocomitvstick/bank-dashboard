async function loadMoexTradingSummary() {
  const instrument = document.getElementById('moexSummaryInstr')?.value || 'CNY';
  const tradeDate  = document.getElementById('moexSummaryDate')?.value;
  const el         = document.getElementById('moexSummaryContent');
  if (!el) return;

  el.innerHTML = '<span class="mkt-no-data">Загрузка...</span>';

  try {
    const params = tradeDate ? `?instrument=${instrument}&trade_date=${tradeDate}` : `?instrument=${instrument}`;
    const data   = await apiFetch(`/moex/trading-summary${params}`);
    if (!data) { el.innerHTML = '<span class="mkt-no-data">Нет данных</span>'; return; }

    // Кол-во знаков после запятой: для металлов 4, для валют 4
    const fmt  = (v, dec=4) => v != null ? v.toLocaleString('ru-RU', {minimumFractionDigits:dec, maximumFractionDigits:dec}) : '—';
    const fmtI = (v) => v != null ? v.toLocaleString('ru-RU') : '—';

    const items = [];
    const addItem = (label, val, extraClass='') => {
      items.push(`<div class="moex-summary-item ${extraClass}"><div class="moex-summary-label">${label}</div><div class="moex-summary-val">${val}</div></div>`);
    };

    // Изменение за день (цветной блок)
    if (data.change != null && data.change_pct != null) {
      const sign    = data.change >= 0 ? '+' : '';
      const color   = data.change >= 0 ? '#4caf8a' : '#e05c5c';
      const arrow   = data.change >= 0 ? '▲' : '▼';
      const chgStr  = `<span style="color:${color};font-weight:600">${arrow} ${sign}${fmt(data.change)} ₽ &nbsp;(${sign}${data.change_pct.toLocaleString('ru-RU', {minimumFractionDigits:2,maximumFractionDigits:2})}%)</span>`;
      addItem(`Изменение (vs ${data.prev_date || 'пред. день'})`, chgStr);
      if (data.prev_close != null) addItem('Предыдущая цена', fmt(data.prev_close) + ' ₽');
    }

    // MOEX биржевые (CNY — полный OHLCV)
    if (data.wap  != null) addItem('Средневзв. курс (WAP)',  fmt(data.wap) + ' ₽');
    if (data.open != null) addItem('Цена открытия',    fmt(data.open) + ' ₽');
    if (data.low  != null) addItem('Минимальная цена', fmt(data.low)  + ' ₽');
    if (data.high != null) addItem('Максимальная цена',fmt(data.high) + ' ₽');
    addItem('Цена закрытия / Учётная', fmt(data.close) + ' ₽');
    if (data.volume_rub != null) addItem('Объём торгов (руб.)', fmtI(Math.round(data.volume_rub)) + ' ₽');
    if (data.numtrades  != null) addItem('Кол-во сделок', fmtI(data.numtrades));

    // Металлы ЦБ — цены покупки и продажи
    if (data.buy  != null) addItem('Цена покупки ЦБ',  fmt(data.buy)  + ' ₽/г');
    if (data.sell != null) addItem('Цена продажи ЦБ',  fmt(data.sell) + ' ₽/г');

    const src = `<div class="moex-summary-src">📅 Дата: ${data.date} &nbsp;|&nbsp; 📡 Источник: ${data.source} &nbsp;|&nbsp; 📏 Единица: ${data.unit}</div>`;
    el.innerHTML = items.join('') + src;
  } catch (e) {
    el.innerHTML = '<span class="mkt-no-data">Данные недоступны для выбранной даты</span>';
    console.warn('MOEX summary error:', e);
  }
}

