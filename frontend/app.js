let _f101CompSeries     = {};   // { 'code:ap': [{date, value}, ...] }
let _f101CompMetricKey  = null; // метрика, для которой загружены ряды
let _f101CompLoading    = false;

async function loadF101CompSeries() {
  const comps = F101_COMPONENTS[state.metric];
  if (!comps || !comps.length) { _f101CompSeries = {}; _f101CompMetricKey = null; return; }

  // Не перегружаем если уже загружено для этой метрики + периода + дат
  const cacheKey = `${state.metric}|${state.period}|${state.dateFrom}|${state.dateTo || ''}`;
  if (_f101CompMetricKey === cacheKey && Object.keys(_f101CompSeries).length) return;

  if (_f101CompLoading) return;
  _f101CompLoading = true;
  try {
    const codes = getF101Leaves(state.metric).map(c => `${c.code}:${c.ap}`).join(',');
    const params = new URLSearchParams({
      codes,
      date_from: state.dateFrom,
      date_to:   state.dateTo || new Date().toISOString().slice(0, 10),
      period:    state.period,
    });
    const regNum = (state.kpiBank && state.kpiBank.reg_num) || PSB_REG;
    const resp = await apiFetch(`/banks/${regNum}/f101-timeseries?${params}`);
    _f101CompSeries    = resp.series || {};
    _f101CompMetricKey = cacheKey;
  } catch(e) {
    console.warn('F101 comp series error:', e);
    _f101CompSeries = {};
  } finally {
    _f101CompLoading = false;
  }
}

function rebuildPsbSeriesFromComps() {
  const comps = getF101Leaves(state.metric);
  if (!comps.length || !Object.keys(_f101CompSeries).length) return null;

  // Собираем все даты
  const dateSet = new Set();
  comps.forEach(c => {
    const key = `${c.code}:${c.ap}`;
    if (_f101ActiveCodes.has(key) && _f101CompSeries[key]) {
      _f101CompSeries[key].forEach(pt => dateSet.add(pt.date));
    }
  });

  if (!dateSet.size) return null;

  const dates = [...dateSet].sort();
  const result = dates.map(dt => {
    let total = 0;
    for (const c of comps) {
      const key = `${c.code}:${c.ap}`;
      if (!_f101ActiveCodes.has(key)) continue;
      const series = _f101CompSeries[key];
      if (!series) continue;
      const pt = series.find(p => p.date === dt);
      if (pt != null) total += c.sign * pt.value;
    }
    return { date: dt, value: total };
  });
  return result;
}

