/** Загружает и рисует исторический график выбранного показателя */
async function loadMarketHistory() {
  const indicator = _mktIndicator;
  const dateFrom  = document.getElementById('mktDateFrom')?.value || '2020-01-01';
  const dateTo    = document.getElementById('mktDateTo')?.value   || new Date().toISOString().slice(0,10);
  const loading   = document.getElementById('mktLoading');
  const titleEl   = document.getElementById('mktChartTitle');
  const subEl     = document.getElementById('mktChartSub');
  const noteEl    = document.getElementById('mktChartNote');

  if (loading) loading.classList.remove('hidden');
  if (titleEl) titleEl.textContent = MKT_LABELS[indicator] || indicator;
  if (subEl)   subEl.textContent   = `${dateFrom} — ${dateTo}`;
  if (noteEl)  noteEl.textContent  = '';

  try {
    let url, points = [];
    if (indicator === 'keyrate') {
      url = `/cbr/keyrate-history?date_from=${dateFrom}&date_to=${dateTo}`;
      const resp = await apiFetch(url);
      points = (resp.data || []).map(p => ({ x: p.date, y: p.value }));
      if (noteEl) noteEl.textContent = 'Источник: ЦБ РФ, SOAP DailyInfoWebServ/KeyRate';
    } else if (indicator in MKT_METAL_CODE) {
      const metalCode = MKT_METAL_CODE[indicator];
      url = `/cbr/metals-history?metal=${metalCode}&date_from=${dateFrom}&date_to=${dateTo}`;
      const resp = await apiFetch(url);
      points = (resp.data || []).map(p => ({ x: p.date, y: p.value }));
      if (noteEl) noteEl.textContent = `Источник: ЦБ РФ, SOAP DragMetDynamic. Учётная цена за 1 г, руб.`;
    } else {
      // Валюта
      url = `/cbr/currency-history?code=${indicator}&date_from=${dateFrom}&date_to=${dateTo}`;
      const resp = await apiFetch(url);
      points = (resp.data || []).map(p => ({ x: p.date, y: p.value }));
      if (noteEl) noteEl.textContent = 'Источник: ЦБ РФ, SOAP GetCursDynamic. Курс за 1 единицу, руб.';
    }

    if (!points.length) {
      if (noteEl) noteEl.textContent = '⚠ Нет данных за выбранный период.';
      if (_mktHistoryChart) { _mktHistoryChart.destroy(); _mktHistoryChart = null; }
      return;
    }

    // Сортируем по дате по возрастанию — слева старое, справа новое
    points.sort((a, b) => a.x.localeCompare(b.x));

    // Статистика
    const vals = points.map(p => p.y);
    const minV = Math.min(...vals), maxV = Math.max(...vals);
    const first = vals[0], last = vals[vals.length-1];
    const delta = ((last - first) / first * 100).toFixed(1);
    const sign  = delta >= 0 ? '+' : '';
    if (subEl) subEl.textContent = `${dateFrom} — ${dateTo}   |   мин: ${minV.toFixed(2)}   макс: ${maxV.toFixed(2)}   изм: ${sign}${delta}%`;
    // Дельта-бейдж
    const deltaEl = document.getElementById('mktDeltaBadge');
    if (deltaEl) {
      const deltaNum = parseFloat(delta);
      deltaEl.textContent = `${sign}${delta}%`;
      deltaEl.className = 'mkt-delta-badge ' + (deltaNum > 0 ? 'pos' : deltaNum < 0 ? 'neg' : 'neu');
      deltaEl.style.display = 'inline-block';
    }
    // Excel export
    const exportBtn = document.getElementById('mktExportXls');
    if (exportBtn) {
      exportBtn._mktPoints = points;
      exportBtn._mktLabel  = MKT_LABELS[indicator] || indicator;
      // Ключевая ставка — ступенчатый ряд, экспортируем только даты изменений
      exportBtn._mktIsStep = (indicator === 'keyrate');
    }

    const canvas = document.getElementById('mktHistoryChart');
    if (!canvas) return;
    if (_mktHistoryChart) _mktHistoryChart.destroy();

    const isRate = indicator === 'keyrate';
    _mktHistoryChart = new Chart(canvas.getContext('2d'), {
      type: 'line',
      data: {
        labels: points.map(p => p.x),
        datasets: [{
          label: MKT_LABELS[indicator] || indicator,
          data: points.map(p => p.y),
          borderColor: isRate ? '#ff7043' : '#00d4ff',
          borderWidth: 2,
          pointRadius: points.length > 200 ? 0 : 2,
          pointBackgroundColor: isRate ? '#ff7043' : '#00d4ff',
          tension: 0.2,
          fill: true,
          backgroundColor: isRate ? 'rgba(255,112,67,0.07)' : 'rgba(0,212,255,0.06)',
        }]
      },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        interaction: { intersect: false, mode: 'index' },
        plugins: {
          legend: { display: false },
          tooltip: {
            enabled: true,
            backgroundColor: '#0d1525',
            borderColor: '#1e2d45',
            borderWidth: 1,
            titleColor: '#c8d8e8',
            bodyColor: '#7a9ab8',
            padding: 10,
            callbacks: {
              title: items => items[0]?.label || '',
              label: ctx => {
                const unit = isRate ? '%' : (indicator in MKT_METAL_CODE ? ' ₽/г' : ' ₽');
                return ` ${ctx.parsed.y.toFixed(2)}${unit}`;
              }
            }
          }
        },
        scales: {
          x: {
            type: 'category',
            ticks: { color: '#666', font: { size: 9 }, maxTicksLimit: 12, maxRotation: 0 },
            grid: { color: '#1a1a1a' }
          },
          y: {
            ticks: {
              color: '#666', font: { size: 9 },
              callback: v => isRate ? v + '%' : (indicator in MKT_METAL_CODE ? v.toFixed(0) + ' ₽' : v.toFixed(2) + ' ₽')
            },
            grid: { color: '#1a1a1a' }
          }
        }
      }
    });
  } catch(e) {
    if (noteEl) noteEl.textContent = `⚠ Ошибка загрузки: ${e.message}`;
    console.warn('Market history error:', e);
  } finally {
    if (loading) loading.classList.add('hidden');
  }
}

