function switchMetricToMethodology(method) {
  const cur = state.metric;
  let newMetric = cur;

  if (method === 'cbr') {
    // Банки.ру → ЦБ: если есть CBR-аналог, переключаем
    if (METRIC_PAIRS[cur]) {
      newMetric = METRIC_PAIRS[cur];
    }
  } else {
    // ЦБ → Банки.ру: если текущая метрика — CBR, переключаем обратно
    if (METRIC_PAIRS_REV[cur]) {
      newMetric = METRIC_PAIRS_REV[cur];
    }
  }

  if (newMetric !== cur) {
    state.metric = newMetric;
    document.getElementById('selMetric').value = newMetric;
  }
  updateMetricDescription();
  debounce(refreshAll);
}

