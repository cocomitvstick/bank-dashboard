/** Получить выбранные металлы из чекбоксов */
function getSelectedMetals() {
  const checks = document.querySelectorAll('#metalsFilters input[data-metal]');
  const selected = [];
  checks.forEach(cb => {
    if (cb.checked) {
      const m = METALS_INFO.find(mi => mi.code === cb.dataset.metal);
      if (m) selected.push(m);
    }
  });
  return selected.length ? selected : METALS_INFO; // fallback — все
}

async function loadMetalsChart() {
  // Используем собственные поля дат для металлов
  const dateFrom = document.getElementById('metalsDateFrom')?.value || '2020-01-01';
  const dateTo   = document.getElementById('metalsDateTo')?.value   || new Date().toISOString().slice(0,10);
  const loading  = document.getElementById('mktMetalsLoading');
  const canvas   = document.getElementById('metalsChart');
  if (!canvas) return;

  const selectedMetals = getSelectedMetals();
  if (loading) loading.classList.remove('hidden');

  try {
    // Загружаем только выбранные металлы
    const results = await Promise.all(
      selectedMetals.map(m =>
        apiFetch(`/cbr/metals-history?metal=${m.code}&date_from=${dateFrom}&date_to=${dateTo}`)
          .then(r => ({ ...m, points: (r.data || []).map(p => ({ x: p.date, y: p.value })).sort((a,b) => a.x.localeCompare(b.x)) }))
          .catch(() => ({ ...m, points: [] }))
      )
    );

    // Объединённый набор дат
    const dateSet = new Set();
    results.forEach(r => r.points.forEach(p => dateSet.add(p.x)));
    const allDates = [...dateSet].sort();

    if (!allDates.length) {
      if (loading) loading.classList.add('hidden');
      return;
    }

    // Сохраняем для Excel
    _metalsChartData = { metals: results, dates: allDates, dateFrom, dateTo };

    // Определяем нужна ли лог. шкала: если разброс цен > 10x — используем логарифм
    let allVals = [];
    results.forEach(m => m.points.forEach(p => { if (p.y > 0) allVals.push(p.y); }));
    const minVal = Math.min(...allVals);
    const maxVal = Math.max(...allVals);
    const useLog = (maxVal / minVal) > 10;

    const datasets = results.map(m => {
      const map = {};
      m.points.forEach(p => { map[p.x] = p.y; });
      return {
        label: `${m.sym} — ${m.name}`,
        data: allDates.map(d => map[d] ?? null),
        borderColor: m.color,
        borderWidth: 1.5,
        pointRadius: 0,
        tension: 0.2,
        fill: false,
        spanGaps: true,
        yAxisID: 'y',
      };
    });

    const yLabel = results.map(m => m.sym).join(' / ') + ', ₽/г';

    const scales = {
      x: {
        ticks: { color: '#666', font: { size: 9 }, maxTicksLimit: 14, maxRotation: 0 },
        grid: { color: '#1a1a1a' }
      },
      y: {
        type: useLog ? 'logarithmic' : 'linear',
        position: 'left',
        title: { display: true, text: yLabel + (useLog ? ' (лог.)' : ''), color: '#666', font: { size: 9 } },
        ticks: {
          color: '#666', font: { size: 9 },
          callback: function(v) {
            // На лог. шкале Chart.js рисует много промежуточных тиков — оставляем только "круглые"
            if (useLog) {
              if (v >= 1000) return (v / 1000).toLocaleString('ru-RU') + 'к';
              return v.toLocaleString('ru-RU');
            }
            return v.toLocaleString('ru-RU');
          }
        },
        grid: { color: '#1a1a1a' }
      },
    };

    if (_metalsChart) _metalsChart.destroy();
    _metalsChart = new Chart(canvas.getContext('2d'), {
      type: 'line',
      data: { labels: allDates, datasets },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        interaction: { intersect: false, mode: 'index' },
        plugins: {
          legend: { display: true, labels: { color: '#7a9ab8', boxWidth: 14, font: { size: 10 } } },
          tooltip: {
            enabled: true,
            backgroundColor: '#0d1525',
            borderColor: '#1e2d45',
            borderWidth: 1,
            titleColor: '#c8d8e8',
            bodyColor: '#7a9ab8',
            padding: 10,
            callbacks: {
              label: ctx => ` ${ctx.dataset.label}: ${ctx.parsed.y?.toLocaleString('ru-RU', {minimumFractionDigits:2, maximumFractionDigits:2})} ₽/г`
            }
          }
        },
        scales
      }
    });
  } catch (e) {
    console.warn('Metals chart error:', e);
  } finally {
    if (loading) loading.classList.add('hidden');
  }
}

