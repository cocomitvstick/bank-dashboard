function renderTable(banks) {
  const tbody = document.getElementById('rankTableBody');
  if (!banks.length) {
    tbody.innerHTML = '<tr><td colspan="5" style="text-align:center;color:var(--text-3);padding:20px">Нет данных</td></tr>';
    return;
  }

  const maxVal = Math.max(...banks.map(b => Math.abs(b.value || 0)));

  tbody.innerHTML = banks.map(b => {
    const d = fmtDelta(b.delta);
    const barPct = maxVal > 0 ? Math.round(Math.abs(b.value || 0) / maxVal * 100) : 0;
    const isPsb  = b.is_primsocbank;
    return `
      <tr class="${isPsb ? 'psb-row' : ''}">
        <td class="rank-num mono">${b.rank}</td>
        <td class="bank-name-cell">
          <div class="bank-name${isPsb ? ' accent' : ''}">${b.name}${isPsb ? ' ★' : ''}</div>
          <div class="bank-region">${b.region}</div>
        </td>
        <td class="value-cell mono">${fmt(b.value, state.tableMetric)}</td>
        <td class="bar-cell">
          <div class="bar-track">
            <div class="bar-fill${isPsb ? ' psb' : ''}" style="width:${barPct}%"></div>
          </div>
        </td>
        <td class="delta-cell ${d.cls} mono">${d.text}</td>
      </tr>
    `;
  }).join('');
}

