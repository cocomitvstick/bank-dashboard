async function loadSettings() {
  try {
    const s = await apiFetch('/settings');
    const modeMap = { auto: 'connAuto', direct: 'connDirect', proxy: 'connProxy' };
    const radioId = modeMap[s.connection_mode] || 'connAuto';
    const radio = document.getElementById(radioId);
    if (radio) radio.checked = true;
    const timeoutInput = document.getElementById('timeoutInput');
    if (timeoutInput) timeoutInput.value = s.timeout || 15;
    document.getElementById('proxyUrlRow')?.classList.toggle('hidden', s.connection_mode !== 'proxy');
    // Парсим сохранённый proxy_url обратно в поля
    const proxyUrl = s.proxy_url || '';
    if (proxyUrl) {
      try {
        // Формат: type://login:pass@host:port
        const u = new URL(proxyUrl);
        const typeEl = document.getElementById('proxyType');
        if (typeEl) typeEl.value = u.protocol.replace(':', '');
        const hostEl = document.getElementById('proxyHost');
        if (hostEl) hostEl.value = u.host; // host:port
        const loginEl = document.getElementById('proxyLogin');
        if (loginEl) loginEl.value = decodeURIComponent(u.username || '');
        const passEl = document.getElementById('proxyPass');
        if (passEl) passEl.value = decodeURIComponent(u.password || '');
      } catch(_) {
        // Если URL не парсится — просто показываем как есть в хосте
        const hostEl = document.getElementById('proxyHost');
        if (hostEl) hostEl.value = proxyUrl;
      }
    }
  } catch(e) {
    console.warn('Settings load error:', e);
  }
}

async function saveSettings() {
  const mode    = document.querySelector('input[name=connMode]:checked')?.value || 'auto';
  const host    = document.getElementById('proxyHost')?.value?.trim() || '';
  const login   = document.getElementById('proxyLogin')?.value?.trim() || '';
  const pass    = document.getElementById('proxyPass')?.value || '';
  const type    = document.getElementById('proxyType')?.value || 'socks5';
  const timeout = parseInt(document.getElementById('timeoutInput')?.value || '15');
  const statusEl = document.getElementById('settingsStatus');
  // Собираем proxy_url из полей
  let proxyUrl = '';
  if (host) {
    const cred = login ? `${encodeURIComponent(login)}:${encodeURIComponent(pass)}@` : '';
    proxyUrl = `${type}://${cred}${host}`;
  }
  if (statusEl) { statusEl.style.color = ''; statusEl.textContent = 'Сохранение...'; }

  try {
    const result = await fetch(`${API}/settings`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ connection_mode: mode, proxy_url: proxyUrl, timeout }),
    });
    if (!result.ok) {
      const err = await result.json().catch(() => ({}));
      if (statusEl) statusEl.textContent = `Ошибка: ${err.detail || result.status}`;
      return;
    }
    if (statusEl) {
      statusEl.style.color = '#4caf8a';
      statusEl.textContent = '✓ Настройки сохранены и применены';
    }
    setTimeout(() => document.getElementById('settingsModal')?.classList.add('hidden'), 1200);
  } catch(e) {
    if (statusEl) statusEl.textContent = 'Ошибка сохранения: ' + e.message;
  }
}

