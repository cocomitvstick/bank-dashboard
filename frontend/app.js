function exportFxXls() {
  if (!_moexFxData?.points?.length) return;
  const { currency, points } = _moexFxData;
  const hasFull = points[0]?.open !== undefined;
  let hdr = `<Row><Cell><Data ss:Type="String">Дата</Data></Cell>`;
  if (hasFull) hdr += `<Cell><Data ss:Type="String">Открытие</Data></Cell><Cell><Data ss:Type="String">Макс</Data></Cell><Cell><Data ss:Type="String">Мин</Data></Cell><Cell><Data ss:Type="String">Ср.взв.</Data></Cell>`;
  hdr += `<Cell><Data ss:Type="String">Закрытие</Data></Cell></Row>`;
  const rows = points.map(p => {
    let r = `<Row><Cell><Data ss:Type="String">${p.date}</Data></Cell>`;
    if (hasFull) r += `<Cell><Data ss:Type="Number">${p.open??''}</Data></Cell><Cell><Data ss:Type="Number">${p.high??''}</Data></Cell><Cell><Data ss:Type="Number">${p.low??''}</Data></Cell><Cell><Data ss:Type="Number">${p.wap??''}</Data></Cell>`;
    r += `<Cell><Data ss:Type="Number">${p.close??''}</Data></Cell></Row>`;
    return r;
  }).join('');
  const xml = `<?xml version="1.0"?><Workbook xmlns="urn:schemas-microsoft-com:office:spreadsheet" xmlns:ss="urn:schemas-microsoft-com:office:spreadsheet"><Worksheet ss:Name="${currency}"><Table>${hdr}${rows}</Table></Worksheet></Workbook>`;
  const blob = new Blob([xml], { type: 'application/vnd.ms-excel' });
  const a = document.createElement('a'); a.href = URL.createObjectURL(blob);
  a.download = `MOEX_${currency}.xls`; document.body.appendChild(a); a.click(); document.body.removeChild(a);
}

