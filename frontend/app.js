document.getElementById('rankTable').addEventListener('click', e => {
  const th = e.target.closest('th[data-col]');
  if (!th) return;
  const col = th.dataset.col;
  if (col === state.tableSortCol) {
    state.tableSortDir = state.tableSortDir === 'asc' ? 'desc' : 'asc';
  } else {
    state.tableSortCol = col;
    state.tableSortDir = 'asc';
  }
  document.querySelectorAll('#rankTable th').forEach(t => t.classList.remove('sorted'));
  th.classList.add('sorted');
  th.querySelector('.sort-icon').textContent = state.tableSortDir === 'asc' ? '↓' : '↑';

  const sorted = [..._tableData].sort((a, b) => {
    let va = col === 'rank' ? a.rank : (col === 'value' ? a.value : a.name);
    let vb = col === 'rank' ? b.rank : (col === 'value' ? b.value : b.name);
    if (typeof va === 'string') va = va.toLowerCase();
    if (typeof vb === 'string') vb = vb.toLowerCase();
    if (va == null) return 1;
    if (vb == null) return -1;
    return state.tableSortDir === 'asc' ? (va > vb ? 1 : -1) : (va < vb ? 1 : -1);
  });
  renderTable(sorted);
});

