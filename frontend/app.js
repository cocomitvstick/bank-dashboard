function escXml(s) {
  return String(s ?? '').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

function exportChartXLS() {
  if (!_chartRawData) {
    toast('Сначала загрузите данные графика', 'error');
    return;
  }
  const { metricLabel, unit, labels, series, period, dateFrom } = _chartRawData;
  const periodLabels = { month: 'Месяц', quarter: 'Квартал', year: 'Год' };
  const today = new Date().toISOString().slice(0, 10);

  // Заголовок файла
  const title = `ПСБ Analytics — Динамика: ${metricLabel} (${unit || 'значение'})`;
  const subtitle = `Период: ${periodLabels[period] || period} | С ${dateFrom} по ${today} | Экспорт: ${today}`;

  // Строим XML
  let rows = '';

  // Строка с заголовком документа
  const titleCells = `<Cell ss:StyleID="title"><Data ss:Type="String">${escXml(title)}</Data></Cell>`;
  rows += `<Row>${titleCells}</Row>`;
  const subtitleCells = `<Cell ss:StyleID="sub"><Data ss:Type="String">${escXml(subtitle)}</Data></Cell>`;
  rows += `<Row>${subtitleCells}</Row>`;
  rows += `<Row/>`; // пустая строка

  // Шапка: Дата | Название1 | Название2 | ...
  const headerNames = ['Дата', ...series.map(s => s.name)];
  const headerCells = headerNames.map(h =>
    `<Cell ss:StyleID="hdr"><Data ss:Type="String">${escXml(h)}</Data></Cell>`
  ).join('');
  rows += `<Row>${headerCells}</Row>`;

  // Единицы измерения
  const unitCells = ['', ...series.map(() => unit)].map(u =>
    `<Cell ss:StyleID="sub"><Data ss:Type="String">${escXml(u)}</Data></Cell>`
  ).join('');
  rows += `<Row>${unitCells}</Row>`;

  // Данные
  labels.forEach((label, i) => {
    const dateCells = `<Cell><Data ss:Type="String">${escXml(label)}</Data></Cell>`;
    const valCells = series.map(s => {
      const v = s.data[i]?.value;
      return v != null
        ? `<Cell><Data ss:Type="Number">${Number(v).toFixed(2)}</Data></Cell>`
        : `<Cell><Data ss:Type="String"></Data></Cell>`;
    }).join('');
    rows += `<Row>${dateCells}${valCells}</Row>`;
  });

  const colWidth = `<Column ss:Width="90"/>`;
  const cols = `<Column ss:Width="75"/>${colWidth.repeat(series.length)}`;

  const xml = `<?xml version="1.0" encoding="UTF-8"?>\n` +
    `<?mso-application progid="Excel.Sheet"?>\n` +
    `<Workbook xmlns="urn:schemas-microsoft-com:office:spreadsheet"\n` +
    `  xmlns:ss="urn:schemas-microsoft-com:office:spreadsheet"\n` +
    `  xmlns:x="urn:schemas-microsoft-com:office:excel">\n` +
    `<Styles>\n` +
    `  <Style ss:ID="title"><Font ss:Bold="1" ss:Size="12"/></Style>\n` +
    `  <Style ss:ID="sub"><Font ss:Italic="1" ss:Color="#555555"/></Style>\n` +
    `  <Style ss:ID="hdr"><Font ss:Bold="1"/><Interior ss:Color="#E8F0FE" ss:Pattern="Solid"/></Style>\n` +
    `</Styles>\n` +
    `<Worksheet ss:Name="${escXml(metricLabel)}"><Table>\n` +
    `${cols}\n${rows}\n` +
    `</Table></Worksheet>\n` +
    `</Workbook>`;

  const blob = new Blob([xml], { type: 'application/vnd.ms-excel;charset=utf-8' });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href     = url;
  a.download = `psb_${state.metric}_${today}.xls`;
  a.click();
  URL.revokeObjectURL(url);
  toast('Excel файл сохранён', 'ok');
}

