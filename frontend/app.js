function updateMetricDescription() {
  const el = document.getElementById('metricDesc');
  if (!el) return;
  const desc = METRIC_DESCRIPTIONS[state.metric];
  const cur = state.metric;

  // Определяем парную метрику для показа различий
  const pairKey = METRIC_PAIRS_REV[cur] || cur; // ключ в METRIC_PAIRS (всегда banki-версия)
  const hasPair = !!(METRIC_PAIRS[pairKey]);
  const isCbr = !!METRIC_PAIRS_REV[cur];
  const methodLabel = isCbr ? 'ЦБ' : (hasPair ? 'Банки.ру' : '');

  let html = '';
  // Бейдж методологии
  if (methodLabel) {
    const cls = isCbr ? 'cbr' : 'banki';
    html += `<span class="method-badge ${cls}">${methodLabel}</span> `;
  }
  // Описание
  if (desc) {
    html += desc;
  }
  // Описание различий (если есть парная метрика)
  if (hasPair && METHODOLOGY_DIFF[pairKey]) {
    html += `<div class="method-diff">${METHODOLOGY_DIFF[pairKey]}</div>`;
  }

  if (html) {
    el.innerHTML = html;
    el.classList.remove('hidden');
  } else {
    el.classList.add('hidden');
  }
  renderF101Panel();
  loadF101CompSeries();
}

