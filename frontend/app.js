// ── Калькулятор вклада ──
function calcDeposit() {
  const amount = parseFloat(document.getElementById('depAmount').value) || 0;
  const term   = parseInt(document.getElementById('depTerm').value) || 12;
  const capPeriod = parseInt(document.getElementById('depCapitalization').value); // 0=нет, 1,3,6,12
  const earlyRate = parseFloat(document.getElementById('depEarlyRate').value) || 0;
  const earlyKeep = document.getElementById('depEarlyKeepPrev').value === '1';

  // Собираем ставки по периодам
  const rateRows = document.querySelectorAll('#depRateRows .dep-rate-row');
  const rates = [];
  rateRows.forEach(row => {
    const from = parseInt(row.querySelector('.dep-rate-from').value) || 1;
    const to   = parseInt(row.querySelector('.dep-rate-to').value) || term;
    const val  = parseFloat(row.querySelector('.dep-rate-val').value) || 0;
    rates.push({ from, to, rate: val });
  });

  // Для каждого месяца определяем ставку
  function getRate(month) {
    for (const r of rates) {
      if (month >= r.from && month <= r.to) return r.rate;
    }
    return rates.length ? rates[rates.length - 1].rate : 0;
  }

  // Расчёт помесячно
  let balance = amount;
  let totalInterest = 0;
  let unCapitalized = 0; // накопленные проценты до капитализации
  const rows = [];
  let lastCapMonth = 0; // последний месяц капитализации

  for (let m = 1; m <= term; m++) {
    const annualRate = getRate(m);
    const monthlyRate = annualRate / 100 / 12;
    const interest = (balance + (capPeriod > 0 ? 0 : 0)) * monthlyRate;

    if (capPeriod === 0) {
      // Без капитализации — проценты отдельно
      totalInterest += interest;
      rows.push({
        month: m, rate: annualRate, interest: interest,
        total: balance, totalWithInterest: balance + totalInterest,
      });
    } else {
      // С капитализацией
      unCapitalized += interest;
      totalInterest += interest;
      const isCapMonth = (m % capPeriod === 0) || (m === term);
      if (isCapMonth) {
        balance += unCapitalized;
        lastCapMonth = m;
        unCapitalized = 0;
      }
      rows.push({
        month: m, rate: annualRate, interest: interest,
        total: balance + unCapitalized,
        totalWithInterest: balance + unCapitalized,
      });
    }

    // Эффективная ставка при досрочном закрытии в этом месяце
    let earlyTotal;
    if (earlyKeep && lastCapMonth > 0) {
      // Сохраняем проценты за завершённые периоды капитализации
      // Пересчитываем только текущий незавершённый период по earlyRate
      const monthsSinceLastCap = m - lastCapMonth;
      const earlyInterestThisPeriod = balance * (earlyRate / 100 / 12) * monthsSinceLastCap;
      earlyTotal = balance + earlyInterestThisPeriod;
    } else {
      // Все проценты пересчитываются по ставке досрочного
      earlyTotal = amount + amount * (earlyRate / 100 / 12) * m;
    }
    const earlyEffRate = ((earlyTotal / amount - 1) / m * 12) * 100;
    rows[rows.length - 1].earlyEffRate = earlyEffRate;
    rows[rows.length - 1].earlyTotal = earlyTotal;
  }

  const finalTotal = capPeriod === 0 ? amount + totalInterest : balance;
  const effRate = ((finalTotal / amount - 1) / term * 12) * 100;

  // Формируем результат
  const summary = document.getElementById('depResultSummary');
  const fmtR = v => v.toLocaleString('ru-RU', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  summary.innerHTML = `
    <div class="calc-summary-row"><span class="calc-summary-label">Сумма вклада</span><span class="calc-summary-value">${fmtR(amount)} ₽</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Срок</span><span class="calc-summary-value">${term} мес.</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Итого на счёте</span><span class="calc-summary-value">${fmtR(finalTotal)} ₽</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Начислено процентов</span><span class="calc-summary-value">${fmtR(totalInterest)} ₽</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Доходность</span><span class="calc-summary-value">${fmtR(totalInterest / amount * 100)}%</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Эффективная ставка (годовая)</span><span class="calc-summary-value" style="color:#66bb6a;font-size:16px">${effRate.toFixed(2)}%</span></div>
    <div class="calc-summary-row"><span class="calc-summary-label">Капитализация</span><span class="calc-summary-value">${capPeriod === 0 ? 'нет' : `каждые ${capPeriod} мес.`}</span></div>
  `;

  const tbody = document.querySelector('#depResultTable tbody');
  tbody.innerHTML = rows.map(r => `<tr>
    <td>${r.month}</td>
    <td>${r.rate.toFixed(1)}%</td>
    <td>${fmtR(r.interest)}</td>
    <td>${fmtR(r.totalWithInterest || r.total)}</td>
    <td title="При закрытии в этом месяце: ${fmtR(r.earlyTotal)} ₽">${r.earlyEffRate.toFixed(2)}%</td>
  </tr>`).join('');

  document.getElementById('depResult').style.display = 'block';
}

