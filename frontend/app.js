// Форма 101 — панель разреза по кодам счетов
// ─────────────────────────────────────────────────────────
let _f101Cache     = null;   // { 'numsc:ap': {bln, thr} }
let _f101CacheKey  = null;   // 'regNum:dt'
const _f101ActiveCodes = new Set();

async function getF101Data(regNum, dt) {
  const cacheKey = `${regNum}:${dt || 'latest'}`;
  if (_f101CacheKey === cacheKey && _f101Cache) return _f101Cache;
  try {
    const params = new URLSearchParams({ fmt: 'json' });
    if (dt) params.set('dt', dt);
    const resp = await apiFetch(`/banks/${encodeURIComponent(regNum)}/form101?${params}`);
    const arr  = Array.isArray(resp) ? resp : (resp?.rows || []);
    const lookup = {};
    arr.forEach(r => {
      lookup[`${r.numsc}:${r.ap}`] = { bln: r.vitg_bln ?? 0, thr: r.oborot_bln ?? 0 };
    });
    _f101Cache    = lookup;
    _f101CacheKey = cacheKey;
    return lookup;
  } catch (e) {
    console.warn('F101 fetch error:', e);
    return {};
  }
}

function recalcF101Metric(f101Lookup) {
  const comps = getF101Leaves(state.metric);
  if (!comps.length || !f101Lookup) return null;
  let total = 0, hasAny = false;
  for (const c of comps) {
    const key = `${c.code}:${c.ap}`;
    if (!_f101ActiveCodes.has(key)) continue;
    const entry = f101Lookup[key];
    if (entry !== undefined) {
      total += c.sign * entry.bln;
      hasAny = true;
    }
  }
  return hasAny ? total : null;
}

